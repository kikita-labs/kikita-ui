/** Preferred side where a popover should appear relative to its trigger. */
export type KuiPopoverPlacement = 'top' | 'bottom' | 'left' | 'right';

/** Cross-axis alignment of a popover relative to its trigger. */
export type KuiPopoverAlign = 'start' | 'center' | 'end';

/** User interaction that opens a popover. */
export type KuiPopoverTriggerType = 'click' | 'hover';
